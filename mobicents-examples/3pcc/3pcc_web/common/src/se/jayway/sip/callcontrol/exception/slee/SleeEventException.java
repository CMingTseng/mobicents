package se.jayway.sip.callcontrol.exception.slee;

public class SleeEventException extends SleeException {
	private static final long serialVersionUID = -3875066923266252130L;

	public SleeEventException() {
		super();
	}

	public SleeEventException(String message, Throwable cause) {
		super(message, cause);
	}

	public SleeEventException(String message) {
		super(message);
	}

	public SleeEventException(Throwable cause) {
		super(cause);
	}

}
