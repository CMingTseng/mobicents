package se.jayway.sip.callcontrol.exception.slee;

public class SleeConnectionException extends SleeException {
	private static final long serialVersionUID = 7183726543999076989L;

	public SleeConnectionException() {
		super();
	}

	public SleeConnectionException(String message, Throwable cause) {
		super(message, cause);
	}

	public SleeConnectionException(String message) {
		super(message);
	}

	public SleeConnectionException(Throwable cause) {
		super(cause);
	}

}

